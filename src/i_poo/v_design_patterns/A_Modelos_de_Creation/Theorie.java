package i_poo.v_design_patterns.A_Modelos_de_Creation;

public class Theorie {
  /*
  Un patron de création permet de résoudre les problèmes liés à la création et la configuration d'objets.

  Par exemple, une classe nommée RessourcesApplication gérant toutes les ressources de l'application ne doit
  être instanciée qu'une seule et unique fois. Il faut donc empêcher la création intentionnelle ou accidentelle
  d'une autre instance de la classe. Ce type de problème est résolu par le patron de conception "Singleton".

  Les différents patrons de création sont les suivants :

  Dans cette catégorie, il existe 5 modèles principaux :




  Nom	                                                 Rôle
  -------------------------------------------------------------------------------------------------
  Factory (Fabrique)                                  Créer un objet dont le type dépend du contexte

  abstract Factory  (Fabrique abstraite)              Fournir une interface unique pour instancier des objets
                                                      d'une même famille sans avoir à connaître les classes à instancier

  Builder (Monteur)

  Prototype (Prototype)                               Création d'objet à partir d'un prototype

  Singleton (Singleton)                               Classe qui ne pourra avoir qu'une seule instance


  */
}
